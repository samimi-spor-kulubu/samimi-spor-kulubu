## Taekwondo Nedir?

Taekwondo, Kore kökenli geleneksel bir dövüş sanatıdır. Adını üç Korece kelimeden alır: Tae (ayak), Kwon (yumruk), Do (yol). Yani "ayak ve yumruğun yolu" anlamına gelir. Olimpiyat sporlarından biri olmasıyla dünya çapında milyonlarca kişi tarafından yapılmaktadır.

Ama taekwondo sadece bir dövüş sporu değildir. Disiplin, saygı ve özdenetim öğreten bir yaşam felsefesidir. Bu yüzden özellikle çocuklar için bu kadar değerlidir.

